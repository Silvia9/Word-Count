    #include<fstream>
    #include<iostream>
    #include<string.h>
	using namespace std;
	
    int word=0,letter=0,i=0,sentence=0,temp=0;                  //定义各变量 
    char a[10000000];
    
    int main()
	{
	    	
		ifstream infile;
		string filename,function;
	    cout<<"wc.exe";
	    while(cin>>function)
	    {
	    	cin>>filename;                                     
	    	infile.open(filename.c_str(), ios::in);                //打开文件 
	    	if(!infile)                                            //若未打开，则报错 
	    	{
	    		cerr<<"open error!"<<endl;
			}
			
			else
			{
			     if(function=="-c")                                   //搜索对应的功能字符 
			     {
			     	for(i=0;i<10000000;i++)
			     	{
			     		  a[i]=infile.get() ; 
						 	if(((int)a[i]>=33)&&((int)a[i]<=126))      //将字符转化成ASCII码统计除空格、换行符以外的字符个数 
		                    letter++;
					 }
					 cout<<"letter="<<letter<<endl;   
				 }
				 
				 
				 
				 
				 else if(function=="-w")
			     {
			     	for(i=0;i<10000000;i++)
			     	{
			     			 a[i]=infile.get() ; 
							 if((a[i-1]!=' ')&&((a[i]==' ')||(a[i]=='\n')))  //统计包括换行符的单词个数 
		                   	word++;
		                   	if(a[i]=='\n')                              //换行符计算
		                	temp++;            
					 }
					 cout<<"word="<<word-temp<<endl;     
				 }
				 
				 
				  else if(function=="-s")
			     {
			     	for(i=0;i<10000000;i++)
			     	{
			     		    a[i]=infile.get() ; 
						 	if((a[i]=='.')||(a[i]=='!')||(a[i]=='?'))   //统计句子个数 
		                    sentence++;
					 }
				    cout<<"sentence="<<sentence<<endl;    
				 }
				 
				 else
				 cout<<"NO found!"<<endl;
		
	     
			 
			}
				infile.close();
				cout<<"wc.exe";                            //关闭文件并为下一次的统计做准备 
		
		}
		
		      	return 0;
	 } 
